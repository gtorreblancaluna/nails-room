package mx.com.nails_room.forms;

public class FiltroArticulo {
	
	private String articuloIdFiltro;
	private String fechaInicioAltaFiltro;
	private String fechaFinalAltaFiltro;
	private String descripcionFiltro;
	
	public String getArticuloIdFiltro() {
		return articuloIdFiltro;
	}
	public void setArticuloIdFiltro(String articuloIdFiltro) {
		this.articuloIdFiltro = articuloIdFiltro;
	}
	public String getFechaInicioAltaFiltro() {
		return fechaInicioAltaFiltro;
	}
	public void setFechaInicioAltaFiltro(String fechaInicioAltaFiltro) {
		this.fechaInicioAltaFiltro = fechaInicioAltaFiltro;
	}
	public String getFechaFinalAltaFiltro() {
		return fechaFinalAltaFiltro;
	}
	public void setFechaFinalAltaFiltro(String fechaFinalAltaFiltro) {
		this.fechaFinalAltaFiltro = fechaFinalAltaFiltro;
	}
	public String getDescripcionFiltro() {
		return descripcionFiltro;
	}
	public void setDescripcionFiltro(String descripcionFiltro) {
		this.descripcionFiltro = descripcionFiltro;
	}
	
	

}
